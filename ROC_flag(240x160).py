import turtle
t = turtle.Turtle()
t.speed(10)

#red rectangle (240x160)
t.penup()
t.goto(-120,-80)
t.color("red")
t.pendown()
t.begin_fill()
for i in range(2):
    t.forward(240)
    t.left(90)
    t.forward(160)
    t.left(90)
t.end_fill()
t.penup()

#blue rectangle (120x80)
t.goto(-120,0)
t.color("blue")
t.pendown()
t.begin_fill()
for i in range(2):
    t.forward(120)
    t.left(90)
    t.forward(80)
    t.left(90)
t.end_fill()
t.penup()

#white triangle * 12
t.color("white")
t.goto(-60,70)
t.right(105)
for i in range(12):
    t.pendown()
    t.begin_fill()
    t.forward(int(30*((6**0.5)-(2**0.5)))) #30(√6-√2)
    t.left(105)
    t.forward(int(2*(60-30*(3**0.5))))     #2(60-30√3)
    t.left(105)
    t.forward(int(30*((6**0.5)-(2**0.5)))) #30(√6-√2)
    t.end_fill()
    t.penup()
    t.right(120)
    t.forward(int(15*((6**0.5)-(2**0.5)))) #15(√6-√2)
    t.right(120)

#blue circle (r=17)
t.color("blue")
t.left(105)
t.goto(-60,23)
t.pendown()
t.begin_fill()
t.circle(17)
t.end_fill()
t.penup()

#white circle (r=15)
t.color("white")
t.goto(-60,25)
t.pendown()
t.begin_fill()
t.circle(15)
t.end_fill()
t.penup()
t.hideturtle()
turtle.done()